function [ y ] = mdAx(x)
y=zeros(length(x),1);
n=7;
r1=2; r2=0.5; r3=35; r4=0.7; r5=1;
A=[ r1,0,0,0,-1,1,0;
    0,r2,0,0,0,-1,1;
    0,0,r3,0,-1,0,0;
    0,0,0,r4,0,-1,0;
    0,r5,0,0,0,0,-1;
    1,0,1,0,0,0,0;
    1,-1,0,-1,0,0,0];


for i=1:n
    for j=1:n
        if A(i,j)~=0    
            y(i)=y(i)+A(i,j)*x(j);
        end
    end
end
end